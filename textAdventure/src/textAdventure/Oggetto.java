package textAdventure;

public class Oggetto {
	private String nome;
	private String descrizione;
	
	public Oggetto(String nome, String descrizione) {
		this.nome = nome;
		this.descrizione = descrizione;
	}

	public String getNome() {
		return nome;
	}

	public String getDescrizione() {
		return descrizione;
	}
	
	
	
	
}
