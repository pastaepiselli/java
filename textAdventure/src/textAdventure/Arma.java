package textAdventure;

public class Arma {
	private String nome;
	private int dannoMin;
	private int dannoMax;
	public Arma(String nome, int dannoMin, int dannoMax) {
		this.nome = nome;
		this.dannoMin = dannoMin;
		this.dannoMax = dannoMax;
	}
	public String getNome() {
		return nome;
	}
	public int getDannoMin() {
		return dannoMin;
	}
	public int getDannoMax() {
		return dannoMax;
	}
	
	
	
	
	
	
	
}
